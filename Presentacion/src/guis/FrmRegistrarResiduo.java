    private String getNombreQuimicoSeleccionado(){
        int indiceFilaSeleccionada = this.tblQuimicos.getSelectedRow();
        if(indiceFilaSeleccionada != -1){
            DefaultTableModel modelo = (DefaultTableModel)this.tblQuimicos.getModel();
            int indiceColumnaNombre = 0;
            String NombreQuimicoSeleccionado = (String) modelo.getValueAt(indiceFilaSeleccionada, 
                indiceColumnaNombre);
            return NombreQuimicoSeleccionado;
        }else{
            return null;
        }
    }
    
