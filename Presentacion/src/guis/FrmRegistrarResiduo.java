    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        CtrlResiduos ctrlResiduos = new CtrlResiduos();
        DefaultTableModel modeloTablaComponentes = (DefaultTableModel)this.tblComponentes.getModel();
        String nombre = txtNombre.getText();
        String codigo = txtCodigo.getText();
        if(ctrlResiduos.verificarFormato(nombre, modeloTablaComponentes))
        {
            if(ctrlResiduos.verificarDuplicados(codigo, nombre, modeloTablaComponentes))
            {
                if(ctrlResiduos.agregar(codigo, nombre, modeloTablaComponentes))
                {
                    JOptionPane.showMessageDialog(this, "Se agrego correctamente el Residuo", 
                    "Información", JOptionPane.INFORMATION_MESSAGE);
                }
                else
                {
                    JOptionPane.showMessageDialog(this, "No se pudo agregar el Residuo", 
                    "ERROR", JOptionPane.ERROR_MESSAGE);
                }
            }
            else
            {
                JOptionPane.showMessageDialog(this, "Este Residuo ya existe", 
                "Advertencia", JOptionPane.WARNING_MESSAGE);
            }
        }
        else
        {
            JOptionPane.showMessageDialog(this, "Los datos del Residuo tienen el formato incorrecto o la lista de componentes químicos esta vacía", 
                "Advertencia", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

